age = 35

if age > 100:
    print("You are very old,")
    print("Well done!")
elif age > 80:
    print("You are fairly old")
    print("Pretty good!")
elif age > 40:
    print("You are middle-aged")
    print("Never mind")
elif 30 <= age <= 40:
    print("You are in your 30s or early 40s")
    print("Age is just a number!")
else:
    print("You are not very old - yet")
